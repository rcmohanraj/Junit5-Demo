package it.mohanrc;

public class MathUtil {

    public int add(int a, int b) {
        return a+b;
    }

    public int multiply(int a, int b) {
        return a*b;
    }

    public int divide(int a, int b) {
        return a/b;
    }

    public double computeCircleArea(int radius) {
        return Math.PI*radius*radius;
    }
}
